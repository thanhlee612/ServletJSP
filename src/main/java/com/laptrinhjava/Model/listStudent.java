package com.laptrinhjava.Model;

/**
 *
 * @author ASUS
 */
public class listStudent {
    private String id;
    private String name;
    private String address;
    public listStudent(){}

    public listStudent(String id, String name, String address) {
        this.id = id;
        this.name = name;
        this.address = address;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    @Override
    public String toString() {
        return "listStudent{" + "id=" + id + ", name=" + name + ", address=" + address + '}';
    }
    
    
}
